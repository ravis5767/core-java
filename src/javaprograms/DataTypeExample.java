package javaprograms;

class DataTypeExample {
  public static void main(String[] args) {
	// Primitive Data Types
	  boolean b=true;
	  System.out.println("the boolean variable value is :"+b);
	  char c='x';
	  System.out.println("the character variabble valu is :"+c);
	  byte by=11;
	  System.out.println("the byte variable value is : "+by);
	  short s=55;
	  System.out.println("the short variable value is :"+s);
	  int i=49858;
	  System.out.println("the inyteger variable value is :"+i);
	  long l=254659859545l;
	  System.out.println("the long variable value is :"+l);
	  float f=54.55f;
	  System.out.println("the float variable value is :"+f);
	  double d=59885.54525;
	  System.out.println("the double variable value is :"+d);
	  
	  // non primitive data type
	  String str="name";
	  System.out.println("thew String variable vale is :"+str);
}
}
